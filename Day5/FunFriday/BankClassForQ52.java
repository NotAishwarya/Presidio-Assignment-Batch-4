package day5;



public class BankClassForQ52{
	String name;
	int balance;
	
	public BankClassForQ52(String name, int balance) {
		this.name = name;
		this.balance = balance;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getBalance() {
		return this.balance;
	}
	
}